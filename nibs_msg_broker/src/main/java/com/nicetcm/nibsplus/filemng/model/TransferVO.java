package com.nicetcm.nibsplus.filemng.model;

public class TransferVO
{
    private String host;
    private int availableServerPort;
    private String userId;
    private String password;
    private String remotePath;
    private String localPath;
    private String fileName;

    /**
     * @return the host
     */
    public String getHost()
    {
        return host;
    }
    /**
     * @param host the host to set
     */
    public void setHost(String host)
    {
        this.host = host;
    }
    /**
     * @return the availableServerPort
     */
    public int getAvailableServerPort()
    {
        return availableServerPort;
    }
    /**
     * @param availableServerPort the availableServerPort to set
     */
    public void setAvailableServerPort(int availableServerPort)
    {
        this.availableServerPort = availableServerPort;
    }
    /**
     * @return the userId
     */
    public String getUserId()
    {
        return userId;
    }
    /**
     * @param userId the userId to set
     */
    public void setUserId(String userId)
    {
        this.userId = userId;
    }
    /**
     * @return the password
     */
    public String getPassword()
    {
        return password;
    }
    /**
     * @param password the password to set
     */
    public void setPassword(String password)
    {
        this.password = password;
    }
    /**
     * @return the remotePath
     */
    public String getRemotePath()
    {
        return remotePath;
    }
    /**
     * @param remotePath the remotePath to set
     */
    public void setRemotePath(String remotePath)
    {
        this.remotePath = remotePath;
    }
    /**
     * @return the localPath
     */
    public String getLocalPath()
    {
        return localPath;
    }
    /**
     * @param localPath the localPath to set
     */
    public void setLocalPath(String localPath)
    {
        this.localPath = localPath;
    }
    /**
     * @return the fileName
     */
    public String getFileName()
    {
        return fileName;
    }
    /**
     * @param fileName the fileName to set
     */
    public void setFileName(String fileName)
    {
        this.fileName = fileName;
    }
}
